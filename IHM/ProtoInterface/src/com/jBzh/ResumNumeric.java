package com.jBzh;

public class ResumNumeric {
	
	public String resumNom;
	public String resumValeur;
	
	public ResumNumeric(String resumNom, String resumValeur) {
		super();
		this.resumNom = resumNom;
		this.resumValeur = resumValeur;
	}

	public String getResumNom() {
		return resumNom;
	}
	
	public void setResumNom(String resumNom) {
		this.resumNom = resumNom;
	}
	
	public String getResumValeur() {
		return resumValeur;
	}
	
	public void setResumValeur(String resumValeur) {
		this.resumValeur = resumValeur;
	}
	
	

}
