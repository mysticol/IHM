package bean;

public class Info {

	private String nom;
	private String value;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public Info(String nom, String value) {
		this.nom = nom;
		this.value = value;
	}
	public Info() {
		
	}
	
	
	
	
}
