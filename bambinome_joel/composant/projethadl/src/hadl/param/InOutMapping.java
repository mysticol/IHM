package hadl.param;

public class InOutMapping {

	private Integer in;
	private Integer out;
	public Integer getIn() {
		return in;
	}
	public void setIn(Integer in) {
		this.in = in;
	}
	public Integer getOut() {
		return out;
	}
	public void setOut(Integer out) {
		this.out = out;
	}
	public InOutMapping(Integer in, Integer out) {
		super();
		this.in = in;
		this.out = out;
	}
	
	
}
